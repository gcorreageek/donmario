package beans;

public class BeanEnvioOC {

	String cod;
	String emp;
	String nom;
	String mail1;
	String mail2;
	String sexoS;
	String sexoOA;
	String sexoAO;
	String ruta;
	
	
	
	
	
	public BeanEnvioOC(String cod, String emp, String nom, String mail1,
			String mail2, String sexoS, String sexoOA, String sexoAO,
			String ruta) {
		super();
		this.cod = cod;
		this.emp = emp;
		this.nom = nom;
		this.mail1 = mail1;
		this.mail2 = mail2;
		this.sexoS = sexoS;
		this.sexoOA = sexoOA;
		this.sexoAO = sexoAO;
		this.ruta = ruta;
	}
	
	
	public String getCod() {
		return cod;
	}
	public void setCod(String cod) {
		this.cod = cod;
	}
	public String getEmp() {
		return emp;
	}
	public void setEmp(String emp) {
		this.emp = emp;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getMail1() {
		return mail1;
	}
	public void setMail1(String mail1) {
		this.mail1 = mail1;
	}
	public String getMail2() {
		return mail2;
	}
	public void setMail2(String mail2) {
		this.mail2 = mail2;
	}
	public String getSexoS() {
		return sexoS;
	}
	public void setSexoS(String sexoS) {
		this.sexoS = sexoS;
	}
	public String getSexoOA() {
		return sexoOA;
	}
	public void setSexoOA(String sexoOA) {
		this.sexoOA = sexoOA;
	}
	public String getSexoAO() {
		return sexoAO;
	}
	public void setSexoAO(String sexoAO) {
		this.sexoAO = sexoAO;
	}
	public String getRuta() {
		return ruta;
	}
	public void setRuta(String ruta) {
		this.ruta = ruta;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
}
